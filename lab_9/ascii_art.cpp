// Функция для записи выбранного ASCII-арта в файл
void saveImagesToFile(int choice1, int choice2, const std::string& filename) {
    std::ofstream outputFile(filename);

    if (outputFile.is_open()) {
        if (choice1 == 1) {
            outputFile << getImage1() << std::endl;
        } else if (choice1 == 2) {
            outputFile << getImage2() << std::endl;
        } else if (choice1 == 3) {
            outputFile << getImage3() << std::endl;
        } else {
            std::cout << "Неверный выбор первого изображения.\n";
            return;
        }

        outputFile << "\n\n"; // Разделитель между изображениями

        if (choice2 == 1) {
            outputFile << getImage1() << std::endl;
        } else if (choice2 == 2) {
            outputFile << getImage2() << std::endl;
        } else if (choice2 == 3) {
            outputFile << getImage3() << std::endl;
        } else {
            std::cout << "Неверный выбор второго изображения.\n";
            return;
        }

        outputFile.close();
        std::cout << "Изображения успешно записаны в файл " << filename << std::endl;
    } else {
        std::cout << "Не удалось открыть файл для записи.\n";
    }
}
