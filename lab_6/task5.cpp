#include <iostream>
#include <algorithm>
#include <iomanip> // Для форматирования вывода

using namespace std;

void task5() {
    // Размеры отверстия
    int holeHeight, holeLength, holeWidth;

    // Запрашиваем размеры отверстия у пользователя
    cout << "Введите высоту отверстия (мм): ";
    cin >> holeHeight;
    cout << "Введите длину отверстия (мм): ";
    cin >> holeLength;
    cout << "Введите ширину отверстия (мм): ";
    cin >> holeWidth;

    // Размеры кирпича (стандартные размеры)
    const int brickHeight = 65;
    const int brickLength = 250;
    const int brickWidth = 120;

    cout << "\nПроекции кирпича (реальные размеры)\n";
    cout << "*************************************************   *****************\n";
    cout << "*          Сторона No 2 (65мм)                      Сторона No 1 (250мм)   *\n";
    cout << "*                                                                          *\n";
    cout << "*************************************************   *****************\n";
    cout << "*************************************************\n";
    cout << "*                                               *\n";
    cout << "*         Сторона No 3 (120мм)                   *\n";
    cout << "*                                               *\n";
    cout << "*************************************************\n";

    cout << "\nПроекции отверстия (размеры введены Вами)\n";
    cout << "*************************************************   *****************\n";
    cout << "*         Сторона No 2 (" << setw(3) << holeHeight << "мм)                      Сторона No 1 (" << setw(3) << holeLength << "мм)   *\n";
    cout << "*                                                                          *\n";
    cout << "************************************************    ******************\n";
    cout << "*************************************************\n";
    cout << "*                                               *\n";
    cout << "*         Сторона No 3 (" << setw(3) << holeWidth << "мм)                   *\n";
    cout << "*                                               *\n";
    cout << "*************************************************\n";

    // Проверяем, пройдет ли кирпич в отверстие
    if (brickHeight <= holeHeight && brickLength <= holeLength && brickWidth <= holeWidth) {
        cout << "\nКирпич пройдет в отверстие!\n";
    } else {
        cout << "\nКирпич не пройдет в отверстие!\n";
    }

    //return 0; // Больше не нужно возвращать значение
}
