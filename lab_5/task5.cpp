#include <iostream>
#include <sstream>
#include <string>

using namespace std;

//-----------------------------------------------------------------------------
// Функция для проверки, содержит ли строка только пробелы
//-----------------------------------------------------------------------------
bool isOnlySpaces(const string& str) {
    for (char ch : str) {
        if (ch != ' ') {
            return false;
        }
    }
    return true;
}

//-----------------------------------------------------------------------------
// Функция task5 для обработки и очистки строки от лишних пробелов и табуляций
//-----------------------------------------------------------------------------
void task5() {
    string inputStr;
    cout << "Введите строку: ";
    getline(cin, inputStr);

    // Заменяем табуляции на пробелы
    for (char& ch : inputStr) {
        if (ch == '\t') {
            ch = ' ';
        }
    }

    // Проверяем, что строка не состоит только из пробелов после замены табуляций
    if (isOnlySpaces(inputStr)) {
        cout << "Очищенная строка: (пустая)" << endl;
        return; // Завершаем функцию, если строка состоит только из пробелов
    }

    // Используем stringstream для удаления лишних пробелов
    istringstream iss(inputStr);
    string result, word;
    while (iss >> word) {
        result += word + " ";  // Добавляем слово и один пробел
    }

    // Удаляем последний пробел, если строка не пустая
    if (!result.empty()) {
        result.pop_back();
    }

    cout << "Очищенная строка: " << result << endl;
}
